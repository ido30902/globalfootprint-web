export const getBrandById = (id: string): Brand | undefined => {
  return brandsData[id.toLowerCase()];
};

export const getAllBrands = (): Brand[] => {
  return Object.values(brandsData);
};

export const getBrandsByIndustry = (industry: string): Brand[] => {
  return getAllBrands().filter((brand) => brand.industry === industry);
};

export const searchBrands = (query: string): Brand[] => {
  if (!query.trim()) return [];

  const searchTerm = query.toLowerCase();
  const results: Brand[] = [];

  getAllBrands().forEach((brand) => {
    // Exact name match
    if (brand.name.toLowerCase().includes(searchTerm)) {
      results.push(brand);
      return;
    }

    // Alias match
    if (
      brand.aliases.some((alias) => alias.toLowerCase().includes(searchTerm))
    ) {
      results.push(brand);
      return;
    }

    // Industry match
    if (brand.industry.toLowerCase().includes(searchTerm)) {
      results.push(brand);
    }
  });

  return results;
};
